// ✅ متغيرات عامة
let questions = [];
let remaining = [];
let score = 0;
let timer = 30;
let timerInterval;
let current;

// ✅ عناصر HTML
const qBox = document.getElementById('question');
const answersBox = document.getElementById('answers');
const scoreBox = document.getElementById('score');
const finalBox = document.getElementById('final');
const timerBox = document.getElementById('timer');
const quizBox = document.getElementById('quiz-box');

// ✅ دالة البداية
function startQuiz(topic) {
  if (topic === 'salah') {
    questions = salahQuestions;
  } else if (topic === 'sawm') {
    questions = sawmQuestions;
  } else if (topic === 'zakat') {
    questions = zakatQuestions;
  } else if (topic === 'tawhid') {
    questions = tawhidQuestions;
  }
  if (topic === 'sirah') {
    questions = sirahQuestions;
  }  
  if (topic === 'quran') {
    questions = quranQuestions;
  }  
  remaining = [...questions];
  score = 0;

  document.querySelectorAll('.start-btn').forEach(b => b.style.display = 'none');
  quizBox.style.display = 'block';
  showQuestion();
}

// ✅ دالة التايمر
function startTimer() {
  timer = 30;
  timerBox.innerText = timer;
  timerInterval = setInterval(() => {
    timer--;
    timerBox.innerText = timer;
    if (timer <= 0) {
      clearInterval(timerInterval);
      showQuestion();
    }
  }, 1000);
}

// ✅ عرض سؤال جديد
function showQuestion() {
  clearInterval(timerInterval);

  if (remaining.length === 0) {
    qBox.style.display = 'none';
    answersBox.style.display = 'none';
    scoreBox.style.display = 'none';
    timerBox.style.display = 'none';
    finalBox.style.display = 'block';
    finalBox.innerText = `🎉 انتهى الكويز! مجموع نقاطك: ${score} من ${questions.length}`;
    return;
  }

  current = remaining.shift();

  qBox.style.display = 'block';
  answersBox.style.display = 'block';
  scoreBox.style.display = 'block';
  timerBox.style.display = 'block';

  qBox.innerText = current.q;
  answersBox.innerHTML = '';

  current.a.forEach((ans, idx) => {
    const btn = document.createElement('button');
    btn.innerText = ans;
    btn.className = 'answer';
    btn.onclick = () => checkAnswer(btn, idx);
    answersBox.appendChild(btn);
  });

  startTimer();
}

// ✅ تحقق من الإجابة
function checkAnswer(button, idx) {
  clearInterval(timerInterval);

  if (idx === current.c) {
    button.classList.add('correct');
    score++;
  } else {
    button.classList.add('wrong');
    const buttons = document.querySelectorAll('.answer');
    buttons[current.c].classList.add('correct');
  }

  scoreBox.innerText = `النقاط: ${score}`;
  disableButtons();
  setTimeout(showQuestion, 1000);
}

// ✅ تعطيل الأزرار بعد الإجابة
function disableButtons() {
  const buttons = document.querySelectorAll('.answer');
  buttons.forEach(btn => btn.disabled = true);
}
